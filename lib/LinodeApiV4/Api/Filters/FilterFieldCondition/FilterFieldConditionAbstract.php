namespace iDimensionz\LinodeApiV4\Api\Filters\FilterFieldCondition;

use iDimensionz\LinodeApiV4\Api\Filters\FilterConditionAbstract;

class FilterFieldConditionAbstract extends FilterConditionAbstract
{
    /**
     * @var string
     */
    private $field;

    public function __construct($field, $value, $valueType)
    {
        parent::__construct($value, $valueType);
        $this->setField($field);
    }

    /**
     * @return string
     */
    public function getField()
    {
        return $this->field;
    }

    /**
     * @param string $field
     */
    public function setField($field)
    {
        $this->field = (string) $field;
    }

    /**
     * Specify data which should be serialized to JSON
     * @link  http://php.net/manual/en/jsonserializable.jsonserialize.php
     * @return mixed data which can be serialized by <b>json_encode</b>,
     * which is a value of any type other than a resource.
     * @since 5.4.0
     */
    public function jsonSerialize()
    {
        return [$this->getField() => $this->getValue()];
    }
}
